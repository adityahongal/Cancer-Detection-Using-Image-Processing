% --- Executes on button press in pushbutton4.
function pushbutton4_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton4 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global I
global outp
se = strel('disk', 20);
    Io = imopen(I, se);
     imshow(Io,'parent',handles.axes3); 
     pause(2)  
    Ie = imerode(I, se);
    Iobr = imreconstruct(Ie, I);
    imshow(Iobr,'parent',handles.axes3)
    title('Opening-by-reconstruction (Iobr)','parent',handles.axes3);
    pause(2)
   Ioc = imclose(Io, se);    
        imshow(Ioc,'parent',handles.axes3);
        title('Opening-closing (Ioc)','parent',handles.axes3);    
    Iobrd = imdilate(Iobr, se);
    Iobrcbr = imreconstruct(imcomplement(Iobrd), imcomplement(Iobr));
    Iobrcbr = imcomplement(Iobrcbr);    
        imshow(Iobrcbr,'parent',handles.axes3);
        title('Opening-closing by reconstruction (Iobrcbr)','parent',handles.axes3);
        pause(2)
     fgm = imregionalmax(Iobrcbr);
      imshow(fgm,'parent',handles.axes3);
      title('Regional maxima','parent',handles.axes3);
      I2 = I;
    I2(fgm) = 255;
    imshow(I2,'parent',handles.axes3)
    title('Regional maxima','parent',handles.axes3);
     se2 = strel(ones(5,5));
    fgm2 = imclose(fgm, se2);
    fgm3 = imerode(fgm2, se2);    
    fgm4 = bwareaopen(fgm3, 20);
    I3 = I;
    I3(fgm4) = 200;    
        imshow(I3,'parent',handles.axes3);
    title('Modified regional maxima ','parent',handles.axes3);
    
    
    for ii = 1:size(fgm4,1)
        for jj = 1:size(fgm4,2)
            if fgm4(ii,jj) == 1
                outp(ii,jj) = I(ii,jj);
            else
                outp(ii,jj) = 0;
            end
        end
    end
      imshow(outp,[],'parent',handles.axes3);
    title('Modified regional maxima','parent',handles.axes3);
    bw1 = double(fgm);
    bw1 = im2bw(bw1);
        %imshow(I), title('Objects in cluster 1');
            boundaries = bwboundaries(bw1);
    numberOfBoundaries = size(boundaries);
    for k = 1 : numberOfBoundaries
        thisBoundary= boundaries{k};
        
        end


